let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);


console.log(a == 10); // pregunta es es exactamente igual que el valor y no el tipo
console.log(a != 10); // a es distinto de 10
console.log(a === '10') // pregunta si es exactamente igual a valor y el tipo de valor
console.log(a !== '10') // a es distinto de 10 eb valor y tipo


